    private void txtPretragaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPretragaKeyReleased
        String param = txtPretraga.getText();
        TableModelOrganizacije tmo = (TableModelOrganizacije) tblOrganizacija.getModel();
        tmo.setParametar(param);
        if (tmo.getRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Nema organizacije za zadati kriterijum!");
            txtPretraga.setText("");
            tmo.setParametar("");
            txtPretraga.requestFocusInWindow();
            txtPretraga.selectAll();
        }
    }//GEN-LAST:event_txtPretragaKeyReleased

