    void refreshTable() {
        TableModelOrganizacije model = (TableModelOrganizacije) tblOrganizacija.getModel();
        model.refreshTable();
    }

    private void detalji() {
        int row = tblOrganizacija.getSelectedRow();
        if(row >= 0){
            try {
                Organizacija o =
                        ((TableModelOrganizacije)tblOrganizacija.getModel()).getSelectedOrganizacija(row);
                Organizacija organizacija = ClientController.getInstance().getOrganizacija(o);
                FormDetaljiOrganizacije fdo = new FormDetaljiOrganizacije(this, rootPaneCheckingEnabled, organizacija);
                fdo.setLocationRelativeTo(null);
                fdo.setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Greska prilikom ucitavanja organizacije: " + e.getMessage());
                e.printStackTrace();
            }
        } else{
            JOptionPane.showMessageDialog(rootPane, "Nije oznacena nijedna organizacija u tabeli!");
        }
    }


