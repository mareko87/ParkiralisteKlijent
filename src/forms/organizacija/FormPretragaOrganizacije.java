    private void txtPretragaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPretragaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER || evt.getKeyCode() == KeyEvent.VK_UP || evt.getKeyCode() == KeyEvent.VK_DOWN) {
            if (tblOrganizacija.getRowCount() == 0) {
                return ;
            }
            tblOrganizacija.requestFocusInWindow();
        }
    }//GEN-LAST:event_txtPretragaKeyPressed

    private void btnDetaljiKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnDetaljiKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            btnDetalji.doClick();
        }
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            txtPretraga.requestFocusInWindow();
            txtPretraga.selectAll();
        }
    }//GEN-LAST:event_btnDetaljiKeyPressed



